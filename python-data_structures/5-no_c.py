#!/usr/bin/python3
# Removes all cha c and C form a string

def no_c(my_string):
    {ord('c'): None, ord('C'): None}
    for i in my_string:
        if ord('i') == 99 or 67:
            my_string.translate({ord('c'): None, ord('C'): None})
    return my_string.translate({ord('c'): None, ord('C'): None}) 
